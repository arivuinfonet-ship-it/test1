// Compute Score & Complete Quiz
function finishQuiz() {
  if (timerInterval) clearInterval(timerInterval);
  examFinished = true;
  
  document.getElementById('confirm-view').style.display = 'none';
  document.getElementById('summary-view').style.display = 'block';
  
  let autoScore = 0;
  let maxAutoScore = 0;
  let selfScore = 0;
  let maxSelfScore = 0;
  
  flattenedQuestions.forEach((q) => {
    const ans = answers[q.id];
    if (q.type === 'mcq') {
      maxAutoScore += q.marks;
      if (ans === q.correctAnswer) {
        autoScore += q.marks;
      }
    } else if (q.type === 'fillInBlanks') {
      maxAutoScore += q.marks;
      if (ans && typeof ans === 'object') {
        let correctInputs = 0;
        let inputCount = q.inputs.length;
        q.inputs.forEach((input) => {
          if (ans[input.key] && ans[input.key].trim() === input.expected) {
            correctInputs++;
          }
        });
        autoScore += (correctInputs / inputCount) * q.marks;
      }
    } else if (q.type === 'comprehension') {
      maxAutoScore += q.marks;
      if (ans && typeof ans === 'object') {
        let correctInputs = 0;
        let inputCount = q.subQuestions.length;
        q.subQuestions.forEach((subQ) => {
          if (ans[subQ.id] && ans[subQ.id].trim().includes(subQ.expected.trim())) {
            correctInputs++;
          }
        });
        autoScore += (correctInputs / inputCount) * q.marks;
      }
    } else if (q.type === 'metricalAnalysis') {
      maxAutoScore += q.marks;
      if (ans && typeof ans === 'object') {
        let correctRows = 0;
        const rowMax = 7;
        const keyAnswers = [
          { split: "அற-னீ-னும்", asai: "நிரை நேர் நேர்", vaipaadu: "புளிமாங்காய்" },
          { split: "இன்-ப-மும்", asai: "நேர் நேர் நேர்", vaipaadu: "தேமாங்காய்" },
          { split: "ஈ-னும்", asai: "நேர் நேர்", vaipaadu: "தேமா" },
          { split: "திற-ன-றிந்-து", asai: "நிரை நேர் நேர்", vaipaadu: "புளிமாங்காய்" },
          { split: "தீ-தின்-றி", asai: "நேர் நேர் நேர்", vaipaadu: "தேமாங்காய்" },
          { split: "வந்த", asai: "நேர் நேர்", vaipaadu: "தேமா" },
          { split: "பொருள்", asai: "நிரை", vaipaadu: "மலர்" }
        ];
        keyAnswers.forEach((ansKey, idx) => {
          const userRow = ans[idx];
          if (userRow) {
            const sOk = userRow.split && userRow.split.trim() === ansKey.split;
            const aOk = userRow.asai && (userRow.asai.trim() === ansKey.asai || (idx === 3 && userRow.asai.trim() === "நிரை நிரை நேர்"));
            const vOk = userRow.vaipaadu && (userRow.vaipaadu.trim() === ansKey.vaipaadu || (idx === 3 && userRow.vaipaadu.trim() === "கருவிளங்காய்"));
            if (sOk && aOk && vOk) {
              correctRows++;
            }
          }
        });
        autoScore += (correctRows / rowMax) * q.marks;
      }
    } else {
      // Descriptive, applicationForm, translation
      maxSelfScore += q.marks;
      if (ans && typeof ans === 'object' && ans.selfGrade !== undefined) {
        selfScore += Number(ans.selfGrade);
      }
    }
  });
  
  // Format scores
  autoScore = Math.round(autoScore * 10) / 10;
  selfScore = Math.round(selfScore * 10) / 10;
  const totalScore = Math.round((autoScore + selfScore) * 10) / 10;
  const maxTotalScore = maxAutoScore + maxSelfScore;
  const percentage = Math.round((totalScore / maxTotalScore) * 100);
  
  // Set UI score values
  document.getElementById('summary-score-num').innerText = totalScore;
  document.getElementById('summary-score-den').innerText = `/ ${maxTotalScore}`;
  document.getElementById('summary-auto-score').innerText = `${autoScore} / ${maxAutoScore}`;
  document.getElementById('summary-self-score').innerText = `${selfScore} / ${maxSelfScore}`;
  
  const minTaken = Math.round(timeTakenSec / 60);
  document.getElementById('summary-time').innerText = `${minTaken} நிமி (Min)`;
  
  document.getElementById('summary-mode-lbl').innerText = quizMode === 'exam' ? 'மதிப்பீட்டு அறிக்கை (Evaluation Sheet)' : 'பயிற்சி அறிக்கை (Practice Sheet)';
  
  // Set svg gauge percentage
  document.getElementById('summary-circle-gauge').setAttribute('stroke-dasharray', `${percentage}, 100`);
  
  // Set grading texts
  let title = '';
  let desc = '';
  if (percentage >= 90) {
    title = "அதிசிறந்தது (Outstanding!)";
    desc = "தமிழ் மொழியறிவில் நீங்கள் மிகச்சிறந்த திறமை பெற்றுள்ளீர்கள்.";
  } else if (percentage >= 75) {
    title = "மிக நன்று (Excellent)";
    desc = "உங்களின் மொழித்திறன் பாராட்டத்தக்கதாக உள்ளது.";
  } else if (percentage >= 50) {
    title = "நன்று (Good)";
    desc = "நல்ல முயற்சி. இன்னும் சில பயிற்சிகள் மூலம் முழு மதிப்பெண் பெறலாம்.";
  } else if (percentage >= 35) {
    title = "தேர்ச்சி (Passed)";
    desc = "தேர்ச்சி பெற்றுள்ளீர்கள். இன்னும் கூடுதல் பயிற்சி தேவை.";
  } else {
    title = "மீண்டும் பயிற்சி பெறுக (Needs Practice)";
    desc = "விடாமுயற்சி விஸ்வரூப வெற்றி தரும். மீண்டும் முயற்சிக்கவும்.";
  }
  document.getElementById('summary-grade-title').innerText = title;
  document.getElementById('summary-grade-desc').innerText = desc;
  
  // Trigger Confetti if online library loaded successfully
  if (typeof confetti === 'function' && percentage >= 40) {
    const duration = 3 * 1000;
    const end = Date.now() + duration;

    (function frame() {
      confetti({ particleCount: 3, angle: 60, spread: 55, origin: { x: 0 }, colors: ['#8a4cf6', '#ffd700', '#10b981'] });
      confetti({ particleCount: 3, angle: 120, spread: 55, origin: { x: 1 }, colors: ['#8a4cf6', '#ffd700', '#10b981'] });

      if (Date.now() < end) {
        requestAnimationFrame(frame);
      }
    }());
  }
}

