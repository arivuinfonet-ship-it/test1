// 1. Render MCQs
function renderMcq(q, card) {
  const optionsList = document.createElement('div');
  optionsList.className = 'options-list tamil-text';
  
  const answer = answers[q.id];
  
  q.options.forEach((opt) => {
    const item = document.createElement('div');
    item.className = 'option-item';
    
    // Calculate style classes
    if (quizMode === 'practice' && answer) {
      if (opt.key === q.correctAnswer) {
        item.classList.add('correct');
      } else if (answer === opt.key) {
        item.classList.add('incorrect');
      }
    } else if (answer === opt.key) {
      item.classList.add('selected');
    }
    
    item.innerHTML = `
      <div class="option-key">${opt.key}</div>
      <div class="option-text">${opt.text}</div>
    `;
    
    if (quizMode === 'practice' && answer && opt.key === q.correctAnswer) {
      item.innerHTML += `<span style="color: var(--success); margin-left: auto; font-weight: bold;">✓</span>`;
    } else if (quizMode === 'practice' && answer === opt.key && answer !== q.correctAnswer) {
      item.innerHTML += `<span style="color: var(--danger); margin-left: auto; font-weight: bold;">✗</span>`;
    }
    
    item.onclick = () => {
      if (quizMode === 'practice' && answer) return; // locked
      answers[q.id] = opt.key;
      updateSidebarCompletion(currentIndex, q.id);
      renderQuestion(); // Re-render for feedback
    };
    
    optionsList.appendChild(item);
  });
  
  card.appendChild(optionsList);
  
  // Practice feedback banner
  if (quizMode === 'practice' && answer) {
    const banner = document.createElement('div');
    const correct = answer === q.correctAnswer;
    banner.className = `feedback-box ${correct ? '' : 'feedback-box-error'}`;
    banner.innerHTML = `
      <div class="feedback-text tamil-text">
        <h4 style="color: ${correct ? 'var(--success)' : 'var(--danger)'};">
          ${correct ? 'சரியான விடை!' : 'தவறான விடை!'}
        </h4>
        <p>${q.explanation}</p>
      </div>
    `;
    card.appendChild(banner);
  }
}

// 2. Render Fill-in-the-blanks
function renderFillInBlanks(q, card) {
  const area = document.createElement('div');
  area.className = 'descriptive-area tamil-text';
  
  const wrapper = document.createElement('div');
  wrapper.style.display = 'flex';
  wrapper.style.flexDirection = 'column';
  wrapper.style.gap = '16px';
  
  const currentVal = answers[q.id] || {};
  
  q.inputs.forEach((input) => {
    const val = currentVal[input.key] || '';
    const isCorrect = quizMode === 'practice' && val.trim() === input.expected;
    
    const row = document.createElement('div');
    row.style.display = 'flex';
    row.style.alignItems = 'center';
    row.style.gap = '12px';
    row.style.flexWrap = 'wrap';
    
    row.innerHTML = `
      <span style="font-weight: 500; min-width: 120px;">${input.label}:</span>
    `;
    
    const txtInput = document.createElement('input');
    txtInput.type = 'text';
    txtInput.className = 'form-input';
    txtInput.style.flex = '1';
    txtInput.style.minWidth = '200px';
    txtInput.value = val;
    
    if (quizMode === 'practice' && val) {
      txtInput.classList.add(isCorrect ? 'correct' : 'incorrect');
    }
    
    txtInput.oninput = (e) => {
      answers[q.id] = {
        ...(answers[q.id] || {}),
        [input.key]: e.target.value
      };
      updateSidebarCompletion(currentIndex, q.id);
    };
    
    // In practice mode, check answer on blur to render feedback
    if (quizMode === 'practice') {
      txtInput.onblur = () => renderQuestion();
    }
    
    row.appendChild(txtInput);
    
    if (quizMode === 'practice' && val) {
      const feedback = document.createElement('span');
      feedback.style.fontSize = '0.85rem';
      feedback.style.color = isCorrect ? 'var(--success)' : 'var(--danger)';
      feedback.innerText = isCorrect ? 'சரியானது!' : `விடை: ${input.expected}`;
      row.appendChild(feedback);
    }
    
    wrapper.appendChild(row);
  });
  
  area.appendChild(wrapper);
  card.appendChild(area);
}

