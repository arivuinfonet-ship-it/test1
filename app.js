// Compile flat question list for traversal
let flattenedQuestions = [];

function compileQuestions() {
  flattenedQuestions = [];
  questionsData.parts.forEach((part) => {
    if (part.questions) {
      part.questions.forEach((q) => {
        flattenedQuestions.push({
          ...q,
          partTitle: part.title,
          sectionTitle: ""
        });
      });
    } else if (part.sections) {
      part.sections.forEach((sec) => {
        sec.questions.forEach((q) => {
          flattenedQuestions.push({
            ...q,
            partTitle: part.title,
            sectionTitle: sec.title
          });
        });
      });
    }
  });
}

