function initSignaturePad(canvasId, overlayId, clearId) {
  const canvas = document.getElementById(canvasId);
  const overlay = document.getElementById(overlayId);
  const clear = document.getElementById(clearId);
  if (!canvas) return;
  
  const ctx = canvas.getContext('2d');
  ctx.lineWidth = 2;
  ctx.lineCap = 'round';
  ctx.strokeStyle = '#a374ff';
  
  let isDrawing = false;
  
  const getPos = (e) => {
    const rect = canvas.getBoundingClientRect();
    const clientX = e.clientX || (e.touches && e.touches[0].clientX);
    const clientY = e.clientY || (e.touches && e.touches[0].clientY);
    return {
      x: clientX - rect.left,
      y: clientY - rect.top
    };
  };
  
  const start = (e) => {
    isDrawing = true;
    overlay.style.display = 'none';
    const pos = getPos(e);
    ctx.beginPath();
    ctx.moveTo(pos.x, pos.y);
  };
  
  const draw = (e) => {
    if (!isDrawing) return;
    const pos = getPos(e);
    ctx.lineTo(pos.x, pos.y);
    ctx.stroke();
  };
  
  const stop = () => { isDrawing = false; };
  
  canvas.addEventListener('mousedown', start);
  canvas.addEventListener('mousemove', draw);
  canvas.addEventListener('mouseup', stop);
  canvas.addEventListener('mouseleave', stop);
  
  canvas.addEventListener('touchstart', start);
  canvas.addEventListener('touchmove', draw);
  canvas.addEventListener('touchend', stop);
  
  clear.onclick = () => {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    overlay.style.display = 'block';
  };
}

// 6. Render Descriptive and Translation items
function renderDescriptive(q, card) {
  const area = document.createElement('div');
  area.className = 'descriptive-area tamil-text';
  
  if (q.imageUrl) {
    const visual = document.createElement('div');
    visual.className = 'visual-container';
    visual.innerHTML = `<img src="${q.imageUrl}" alt="Visual prompt" class="visual-image" />`;
    area.appendChild(visual);
  }
  
  const ansObj = answers[q.id] || { text: '', selfGrade: 0 };
  const currentText = typeof ansObj === 'object' ? (ansObj.text || '') : (ansObj || '');
  
  const textarea = document.createElement('textarea');
  textarea.className = 'textarea-input';
  textarea.placeholder = 'இங்கு உங்கள் விடையை எழுதலாம் (You can type your response here...)';
  textarea.value = currentText;
  textarea.oninput = (e) => {
    const textVal = e.target.value;
    const old = answers[q.id] || {};
    answers[q.id] = {
      ...(typeof old === 'object' ? old : { text: old }),
      text: textVal
    };
    updateSidebarCompletion(currentIndex, q.id);
  };
  area.appendChild(textarea);
  
  // Show Answer Key panel
  const toggle = document.createElement('div');
  toggle.className = 'toggle-details';
  
  const header = document.createElement('div');
  header.className = 'toggle-header';
  header.innerHTML = `
    <span style="font-weight: 600; color: var(--accent-gold);">மதிப்பீட்டு விடைக்குறிப்பு (Show Evaluation Key)</span>
    <span id="key-arrow-symbol">▼</span>
  `;
  
  const body = document.createElement('div');
  body.className = 'toggle-body';
  body.style.display = 'none';
  body.innerHTML = `
    <pre>${q.answerKey}</pre>
    <div class="self-grade-box" style="margin-top: 20px; border-top: 1px dotted var(--card-border); padding-top: 20px;">
      <span><strong>சுய மதிப்பீடு (Self Evaluation):</strong> இவ்வினாவுக்கு உங்களின் மதிப்பெண் என்ன?</span>
      <div class="grade-selector" id="grade-selector-container"></div>
    </div>
  `;
  
  header.onclick = () => {
    const open = body.style.display === 'none';
    body.style.display = open ? 'block' : 'none';
    document.getElementById('key-arrow-symbol').innerText = open ? '▲' : '▼';
  };
  
  toggle.appendChild(header);
  toggle.appendChild(body);
  area.appendChild(toggle);
  card.appendChild(area);
  
  // Render grading buttons
  const selector = body.querySelector('#grade-selector-container');
  const currentGrade = typeof answers[q.id] === 'object' ? (answers[q.id].selfGrade || 0) : 0;
  
  for (let g = 0; g <= q.marks; g++) {
    const btn = document.createElement('button');
    btn.type = 'button';
    btn.className = `grade-btn ${currentGrade === g ? 'selected' : ''}`;
    btn.innerText = g;
    btn.onclick = () => {
      const old = answers[q.id] || {};
      answers[q.id] = {
        ...(typeof old === 'object' ? old : { text: old }),
        selfGrade: g
      };
      // Toggle class lists on grade buttons
      const buttons = selector.querySelectorAll('.grade-btn');
      buttons.forEach((b, idx) => {
        b.classList.remove('selected');
        if (idx === g) b.classList.add('selected');
      });
      updateSidebarCompletion(currentIndex, q.id);
    };
    selector.appendChild(btn);
  }
}

